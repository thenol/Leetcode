# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def inorderTraversal(self, root: TreeNode) -> List[int]:
        stack=[]
        ans=[]
        cur=root
        while True:
            if cur:
                stack.append(cur)
                cur=cur.left
            else:
                if stack:
                    x=stack.pop()
                    ans.append(x.val)
                    cur=x.right
                else:
                    break
        return ans